from gtts import gTTS
import os

# ข้อความที่ต้องการแปลงเป็นเสียง
text = "ต้องมีอะไรผิดพลาดตรงไหน ไม่เข้าใจเลยสักครั้ง ไอ้ที่เขาทำฉันนั้นก็ทำ แต่ทำและไม่เคยสมหวัง หรืออาจเป็นเพราะพื้นดวงหรือเปล่า หรือเป็นที่ราศีของดวงดาว เลยทำให้ฉันต้องเหงาอย่างงี้อยู่ใช่ไหม"

# เลือกภาษาให้กับ TTS
language = 'th'  # ภาษาไทย

# สร้างเสียงจากข้อความ
tts = gTTS(text=text, lang=language, slow=False)

# บันทึกไฟล์เสียงเป็น MP3
tts.save("output.mp3")

# เล่นไฟล์เสียงที่สร้างขึ้น
os.system("start output.mp3")
